// = é Atribuição
// == é para comparar o valor
// === é para comparar o valor E FORMATO
// !== é diferente

//Igual
let numero = "1"
let resultado = numero === 1
console.log(resultado)

// Diferente 
let marca = "Apple"
resultado =  marca !== 1
console.log(resultado)

